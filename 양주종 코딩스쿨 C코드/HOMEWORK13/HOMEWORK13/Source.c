// c언어 코딩 :: 실수 입출력 

#include <stdio.h>

int main(void)
{
	float vision;
	puts("------------------");
	puts("** 실수 입출력 **");
	puts("------------------");

	printf("시력 입력: ");
	scanf_s("%f", &vision);

	printf("당신의 시려은 %.1f입니다 \n", vision);
	puts("--------------------------------");
	
	puts("정수, 실수 입력 받을때는 변수 앞에 &를 붙입니다");
	puts("--------------------------------------------");

	return 0;
}