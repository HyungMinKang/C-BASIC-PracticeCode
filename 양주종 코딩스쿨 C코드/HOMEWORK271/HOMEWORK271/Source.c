//비트 NOT(~) 연산자

#include <stdio.h>

int main(void)
{
	puts("\n 비트 NOT(~) 연산자 \n");

	printf("0 = %d \n", 0);
	printf("~0= %d \n", ~0);

	printf("-1=%d \n", -1);
	printf("~-1=%d \n\n", ~- 1);

	printf("5= %d \n", 5);
	printf("~5=%d \n\n", ~5);

	printf(" 8=%d \n", 8);
	printf(" ~8= %d \n\n", ~8);

	return 0;
}