#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>

void insertion(int a[], int n)
{
	int i, j;
	for (i = 1; i < n; i++)
	{
		int tmp = a[0]=a[i];   // j>0 조건을 고려하지 않아도 됨
		for (j = i; a[j - 1] > tmp; j--)
		{
			a[j] = a[j - 1];
		}
		if(j) a[j] = tmp;

	}
}

int main(void)
{
	int i, nx;
	int* x;
	puts("단순 삽입 정렬");

	printf("요소개수: ");
	scanf("%d", &nx);
	x = calloc(nx+1, sizeof(int));

	for (i = 0; i < nx; i++)
	{
		printf("x[%d]: ", i);
		scanf("%d", &x[i]);
	}

	insertion(x, nx);
	puts("오름차순으로 정렬했습니다");
	for (i = 1; i < nx; i++)
	{
		printf("x[%d]: %d\n", i, x[i]);
	}
	free(x);

	return 0;
}